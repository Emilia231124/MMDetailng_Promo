// Утилиты

export function getServiceBySlug(slug: string): ServiceData | undefined {
  return SERVICES.find((s) => s.slug === slug);
}

export function getServicesByCategory(category: ServiceCategoryFilter): ServiceData[] {
  if (category === "Все") return SERVICES;
  return SERVICES.filter((s) => s.category === category);
}

export function getRelatedServices(slug: string, limit = 3): ServiceData[] {
  const service = getServiceBySlug(slug);
  if (!service) return [];
  return SERVICES.filter((s) => s.slug !== slug && s.category === service.category).slice(0, limit);
}

export function formatPrice(n: number): string {
  return n.toLocaleString("ru-RU");
}
